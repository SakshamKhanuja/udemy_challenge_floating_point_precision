public class Main {

    public static void main(String[] args) {
        // Stores the number of pounds.
        double pounds = 247D;

        // Stores the number of kilograms equivalent to those in variable "pounds".
        double poundsConvertedToKilograms = pounds * 0.45359237;

        // Prints the conversion.
        System.out.println(pounds + " pounds = " + poundsConvertedToKilograms + " kilograms");
    }
}
